package ex_04_Operators;

public class Lab038_Unary_Operator {
    public static void main(String[] args) {
        //unary operator
        int a = +10;   //here +10 is optional
        System.out.println(a);

        int b = -110;
        int result = a + b;
        System.out.println(b);
        System.out.println(result);
    }
}
