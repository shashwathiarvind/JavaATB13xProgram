package ex_04_Operators;

public class Lab036_Operators_Arithmetic_Operators {
    public static void main(String[] args) {
        int a = 35;
        int b = 2;
        float c = 3.0f;
        System.out.println(a+b);
        System.out.println("a + b");
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a*b);
        System.out.println(a/c);
    }
}
