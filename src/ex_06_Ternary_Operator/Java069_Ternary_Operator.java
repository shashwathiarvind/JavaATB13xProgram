package ex_06_Ternary_Operator;

public class Java069_Ternary_Operator {
    public static void main(String[] args) {
            int x = 10;
            int y = 30;
            //finding min of 2 numbers using ternary operators
            int min = x < y ? x : y ;
            System.out.println(min);
    }
}
